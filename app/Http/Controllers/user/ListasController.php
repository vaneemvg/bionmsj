    public function index()
    {
        $listas = Lista::with('user')
            ->with('tags')
            ->withCount('contactos')
            ->where('cliente_id',1)//buscar el id que corresponda
            ->paginate(2);

        return view('user.lists.index')->with('listas', $listas);
    }

